import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns

# 1. Setup: Create sample data
# Simulating a dataset: Study hours vs Test Scores

data = pd.read_csv('dataset.csv')


# np.random.seed(42)
# n_samples = 100
# data = {
